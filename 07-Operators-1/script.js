let num1 = 800
let num2 = 5

//divide
let div = num1 / num2
document.writeln(`<br>Division result= ${div}`)

//Remainder
let rem = num1 % num2
document.writeln(`<br>Modulus result= ${rem}`)

//exponents
let expo = num1 ** num2
document.writeln(`<br>Exponents result= ${expo}`)

//increament
let inNum = ++num1 
document.writeln(`<br>Increament result= ${inNum}`)

//decreament
let deNum = --num1 
document.writeln(`<br>Decreament result= ${deNum}`)
